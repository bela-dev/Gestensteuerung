class Position:

    def __init__(self, x, y, z):
        self.x = x
        self.y = y
        self.z = z

    def getX(self):
        return self.x

    def getY(self):
        return self.y

    def getZ(self):
        return self.z

class Position2D:
    def __init__(self, x, y):
        self.x = x;
        self.y = y;

    def getX(self):
        return self.x

    def getY(self):
        return self.y